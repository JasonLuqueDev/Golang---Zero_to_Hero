// Concorrência e Paralelismo

// Go é uma linguagem concorrente

// Paralelismo - Executar código simultaneamente em
// processadores físicos diferentes.

// Concorrência - Intercalar (administrar) vários
// processos ao mesmo tempo e isso pode ocorrer em
// um único processador físico

// Concorrência viabiliza Paralelismo

// É possível que a Concorrência seja melhor que o
// Paralelismo!

// Paralelismo exige muito mais do Sistema Operacional
// e do Hardware.

// Concorrência - É a forma de estruturar o seu programa.
// É a composição de processos (tipicamente funções) que
// executam de forma independente.
